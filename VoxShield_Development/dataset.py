# dataset.py
"""
VoxShield - Dataset Loader Module (Phase 3)
-------------------------------------------
This module implements the PyTorch Dataset for loading ASVspoof audio waveforms,
handling resampling to 16 kHz, amplitude normalization, and repeat-padding/cropping
to exactly 4 seconds (64,000 samples). It returns both raw waveforms and spectral features.

To run a dataset sanity test:
    python -c "import dataset; dataset.run_sanity_check()"

Command-line usage context:
    Implemented as a library module. Imported into training and evaluation scripts.
"""

import os
import pandas as pd
import torch
import torchaudio
from torch.utils.data import Dataset, DataLoader
import config
import features

class ASVspoofDataset(Dataset):
    def __init__(self, manifest_csv, feature_type=None, pad_mode="repeat"):
        """
        Args:
            manifest_csv: Path to the manifest CSV file generated by manifest_builder.py
            feature_type: Override config.FEATURE_TYPE ("lfcc" or "mel")
            pad_mode: "repeat" (repeat audio to reach 4s) or "zero" (zero-padding)
        """
        if not os.path.exists(manifest_csv):
            raise FileNotFoundError(f"Manifest file not found: {manifest_csv}. Run manifest_builder.py first.")
            
        self.df = pd.read_csv(manifest_csv)
        self.feature_type = feature_type if feature_type else config.FEATURE_TYPE
        self.pad_mode = pad_mode.lower()
        self.target_length = config.NUM_SAMPLES  # 64,000 samples

        # Map labels to integers: bonafide -> 0, spoof -> 1
        self.label_map = {"bonafide": 0, "spoof": 1}
        print(f"[Dataset] Loaded {len(self.df)} samples from: {manifest_csv}")

    def __len__(self):
        return len(self.df)

    def _process_audio(self, filepath):
        """Loads, resamples, normalizes, and crops/pads the waveform."""
        # Load waveform
        waveform, sr = torchaudio.load(filepath)
        
        # Convert stereo/multichannel to mono
        if waveform.shape[0] > 1:
            waveform = torch.mean(waveform, dim=0, keepdim=True)
            
        # Resample to 16 kHz if necessary
        if sr != config.SAMPLE_RATE:
            resampler = torchaudio.transforms.Resample(orig_freq=sr, new_freq=config.SAMPLE_RATE)
            waveform = resampler(waveform)
            
        # Remove channel dimension for processing: [T_samples]
        waveform = waveform.squeeze(0)
        
        # Crop or Pad to target length (64,000 samples = 4 seconds)
        num_samples = waveform.shape[0]
        if num_samples >= self.target_length:
            # Slicing: take the first 4 seconds
            waveform = waveform[:self.target_length]
        else:
            # Padding
            num_pad = self.target_length - num_samples
            if self.pad_mode == "repeat":
                # Repeat the clip until it is long enough
                repeats = (self.target_length // num_samples) + 1
                waveform = waveform.repeat(repeats)[:self.target_length]
            else:
                # Zero pad
                waveform = torch.cat([waveform, torch.zeros(num_pad)], dim=0)
                
        # Normalization: Zero-mean, Unit-variance (standard for wav2vec2 inputs)
        mean = waveform.mean()
        std = waveform.std()
        waveform = (waveform - mean) / (std + 1e-9)
        
        return waveform

    def __getitem__(self, idx):
        row = self.df.iloc[idx]
        filepath = row["filepath"]
        utt_id = row["utt_id"]
        label_str = row["label"]
        label = self.label_map[label_str]
        
        # Load and preprocess raw waveform
        try:
            waveform = self._process_audio(filepath)
        except Exception as e:
            # Fallback if audio file is corrupted or missing
            print(f"[Warning] Failed to load {filepath}: {e}. Returning zeros.")
            waveform = torch.zeros(self.target_length)
            
        # Extract features (LFCC or Log-Mel) from the processed waveform
        # Feature shape output: [1, Features, Frames]
        feats = features.extract_features(waveform, feature_type=self.feature_type)
        
        return waveform, feats, label, utt_id

def run_sanity_check():
    """Performs dataset validation, checks class balance and feature shapes."""
    print("=== Running Dataset Sanity Check ===")
    
    # We will test using dev manifest first if available
    manifest_to_check = config.DEV_2019_CSV
    if not os.path.exists(manifest_to_check):
        manifest_to_check = config.TRAIN_2019_CSV
        
    if not os.path.exists(manifest_to_check):
        print(f"[Error] No manifest files found at: {manifest_to_check}")
        print("Please run manifest_builder.py first.")
        return
        
    dataset = ASVspoofDataset(manifest_to_check)
    
    # Check balance
    counts = dataset.df["label"].value_counts()
    print(f"Class Balance in manifest:\n{counts}")
    
    # Check stats
    lengths = []
    print("Checking first 5 files...")
    for i in range(min(5, len(dataset))):
        waveform, feats, label, utt_id = dataset[i]
        print(f"Sample {i} | ID: {utt_id} | Label: {label} | Waveform: {waveform.shape} | Features: {feats.shape}")
        
    print("=== Sanity Check Complete ===")

if __name__ == "__main__":
    run_sanity_check()
